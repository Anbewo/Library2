    public void removeDeletedRow(int rowNumber) {
        
       for(int i = 0; i < 5; i++) {
        userTable.getModel().setValueAt(null, rowNumber, i);
       }
       
    }
    
    public void fillTable(ArrayList delayInfo, ArrayList userInfo) {
        
        int size = delayInfo.size();
        int columns = 8;
        int halfsize = size/columns;
        
       
        int j = 0;
        int x = j;
               
        for (x=0; x < halfsize; x++) {
            for (int i=0; i < columns; i++) {
                delayedTable.getModel().setValueAt(delayInfo.get(j), x, i);
                j++;
            }
        }
        
        size = userInfo.size();
        columns = 5;
        halfsize = size/columns;
        
       
        j = 0;
        x = j;
               
        for (x=0; x < halfsize; x++) {
            for (int i=0; i < columns; i++) {
                userTable.getModel().setValueAt(userInfo.get(j), x, i);
                j++;
            }
        }
        
        wantToChange = true;
        
    }
    
    
    public void emptyTable() {
        
        wantToChange = false;
         
        final String ERASE = null;
        int x = 0;
        int userColumns = 5;
        int delayColumns = 8;
               
        for (x=0; x < 100; x++) {
            for (int j=0; j < userColumns; j++) {
                userTable.getModel().setValueAt(ERASE, x, j);
            }
            for (int i=0; i < delayColumns; i++) {
                delayedTable.getModel().setValueAt(ERASE, x, i);
            }   
        }
        
        rowList.clear();
        
    }
   
    
