    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
      
        emptyTable();
        
        ArrayList<Object> delayInfo = new ArrayList<Object>(); 
        ArrayList<Object> userInfo = new ArrayList<Object>();
        String searchCat = (String)choice.getSelectedItem();
        if(searchCat.equals("AnvändarID")) {
            searchCat = "u.userID";
        }
        if(searchCat.equals("Personnummer")) {
            searchCat = "u.pNr";
        }
        if(searchCat.equals("Förnamn")) {
            searchCat = "u.fName";
        }
        if(searchCat.equals("Efternamn")) {
            searchCat = "u.lName";
        }
        if(searchCat.equals("Epost")) {
            searchCat = "u.email";
        }
        if(searchCat.equals("Kategori")) {
            searchCat = "u.userCat";
        }
        if(searchCat.equals("Streckkod")) {
            searchCat = "l.barcodeID";
        }
        if(searchCat.equals("Titel")) {
            searchCat = "m.title";
        }
        if(searchCat.equals("Återlämningsdatum")) {
            searchCat = "l.return_date";
        }
        String searchVar= searchField.getText();
        
        delayInfo = AdministrationController.listCopies(searchCat, searchVar);
        userInfo = AdministrationController.listUsers(searchCat, searchVar);
        
        fillTable(delayInfo, userInfo);
        
    }//GEN-LAST:event_searchActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        
        Iterator<Integer> iter = rowList.iterator();

        while(iter.hasNext()) {  
          
          int row = iter.next();
          String userID = userTable.getModel().getValueAt(row, 0).toString();
          String fName = userTable.getModel().getValueAt(row, 1).toString();
          String lName = userTable.getModel().getValueAt(row, 2).toString();
          String pNr = userTable.getModel().getValueAt(row, 3).toString();
          String userCat = userTable.getModel().getValueAt(row, 4).toString();
   
          UserController.updateUser(userID, fName, lName, pNr, userCat);
        
        }
    }//GEN-LAST:event_updateActionPerformed

